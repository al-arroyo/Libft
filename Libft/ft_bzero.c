#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*aux;

	aux = (char *)s;
	if (n != 0)
	{
		while (n != 0)
		{
			*aux = '\0';
			aux++;
			n--;
		}
		s = aux;
	}
}
